// Humorous bilingual slurring algorithm for Beer Goggles Mode
function slurText(text, lang) {
  if (!text) return text;
  
  const isVowel = (char) => {
    return /[aeiouyαεηιουωάέήίόύώAEIOUYΑΕΗΙΟΥΩΆΈΉΊΌΎΏ]/.test(char);
  };
  
  const isSlurConsonant = (char) => {
    return /[slrmnσλρμνςSLRMN]/.test(char);
  };

  const words = text.split(' ');
  const slurredWords = words.map((word) => {
    // Skip email addresses, websites, or pure numeric strings (like 5.0% or years)
    if (word.includes('@') || word.includes('http') || /^[0-9%.]+$/.test(word)) {
      return word;
    }

    let slurred = '';
    for (let i = 0; i < word.length; i++) {
      let char = word[i];
      
      // 1. Randomly swap adjacent characters (about 5% chance)
      if (i < word.length - 1 && Math.random() < 0.05) {
        let nextChar = word[i + 1];
        if (/[a-zA-Z\u0370-\u03ff]/.test(char) && /[a-zA-Z\u0370-\u03ff]/.test(nextChar)) {
          slurred += nextChar + char;
          i++; // skip next character
          continue;
        }
      }

      // 2. Random case shifting (about 8% chance)
      if (Math.random() < 0.08 && /[a-zA-Z\u0370-\u03ff]/.test(char)) {
        char = char === char.toUpperCase() ? char.toLowerCase() : char.toUpperCase();
      }

      slurred += char;

      // 3. Vowels doubling/tripling (about 12% chance)
      if (isVowel(char) && Math.random() < 0.12) {
        slurred += char;
        if (Math.random() < 0.4) {
          slurred += char;
        }
      }
      
      // 4. Consonants doubling (about 10% chance)
      if (isSlurConsonant(char) && Math.random() < 0.10) {
        slurred += char.toLowerCase();
      }
    }

    return slurred;
  });

  // 5. Intersperse funny hiccups (about 7% chance between words)
  let resultWords = [];
  const hiccups = lang === 'en' 
    ? ['*hic*', '*urp*', '...hic...', '*burp*'] 
    : ['*γκουπ*', '*χικ*', '...χικ...', '*μπάρπ*'];

  for (let i = 0; i < slurredWords.length; i++) {
    resultWords.push(slurredWords[i]);
    if (i < slurredWords.length - 1 && Math.random() < 0.07) {
      const randomHiccup = hiccups[Math.floor(Math.random() * hiccups.length)];
      resultWords.push(randomHiccup);
    }
  }

  return resultWords.join(' ');
}

function setLanguage(lang) {
  currentLang = lang;
  const text = translations[lang];
  elements.forEach(element => {
    const key = element.dataset.i18n;
    if (key && key in text) {
      const originalText = text[key];
      element.textContent = beerGogglesActive ? slurText(originalText, lang) : originalText;
    }
  });

  // Programmatically translate form placeholders
  const nameInput = document.getElementById('form-name');
  const emailInput = document.getElementById('form-email');
  const messageInput = document.getElementById('form-message');

  if (nameInput) {
    const originalPlaceholder = lang === 'en' ? 'Eleni or John' : 'Ελένη ή Γιάννης';
    nameInput.placeholder = beerGogglesActive ? slurText(originalPlaceholder, lang) : originalPlaceholder;
  }
  if (emailInput) {
    const originalPlaceholder = lang === 'en' ? 'you@example.com' : 'you@example.com';
    emailInput.placeholder = beerGogglesActive ? slurText(originalPlaceholder, lang) : originalPlaceholder;
  }
  if (messageInput) {
    const originalPlaceholder = lang === 'en' ? 'How can we help? Orders, tastings, events...' : 'Πώς μπορούμε να βοηθήσουμε; Παραγγελίες, γευσιγνωσίες, εκδηλώσεις...';
    messageInput.placeholder = beerGogglesActive ? slurText(originalPlaceholder, lang) : originalPlaceholder;
  }

  // Toggle active class on translation buttons
  if (btnEn) btnEn.classList.toggle('active', lang === 'en');
  if (btnGr) btnGr.classList.toggle('active', lang === 'gr');

  if (btnEnAge) btnEnAge.classList.toggle('active', lang === 'en');
  if (btnGrAge) btnGrAge.classList.toggle('active', lang === 'gr');

  document.documentElement.lang = lang === 'en' ? 'en' : 'el';
}

function toggleBeerGoggles() {
  beerGogglesActive = !beerGogglesActive;
  
  const shell = document.querySelector('.page-shell');
  if (shell) {
    if (beerGogglesActive) {
      shell.classList.add('beer-goggles-active');
    } else {
      shell.classList.remove('beer-goggles-active');
    }
  }

  const mapMarkerContainer = document.getElementById('map-marker-container');
  if (mapMarkerContainer) {
    if (beerGogglesActive) {
      mapMarkerContainer.classList.add('beer-goggles-active');
    } else {
      mapMarkerContainer.classList.remove('beer-goggles-active');
    }
  }

  if (btnGogglesTop) btnGogglesTop.classList.toggle('active', beerGogglesActive);
  if (btnGogglesAge) btnGogglesAge.classList.toggle('active', beerGogglesActive);

  // Refresh translated texts with or without slurring
  setLanguage(currentLang);
}

